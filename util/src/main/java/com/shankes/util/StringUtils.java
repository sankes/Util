	// 删除ArrayList中重复元素，保持顺序
	@SuppressWarnings({ "rawtypes", "unchecked" })
	public static void removeDuplicateWithOrder(List list) {
		Set set = new HashSet();
		List newList = new ArrayList();
		for (Iterator iter = list.iterator(); iter.hasNext();) {
			Object element = iter.next();
			if (set.add(element))
				newList.add(element);
		}
		list.clear();
		list.addAll(newList);
		System.out.println(" remove duplicate " + list);
	}

	public static String fromUTF8ToGB2312(String textUTF8){
		String textGB2312="";
		byte[] bb = null;
		try {
			bb = textUTF8.getBytes("UTF-8");
			textGB2312 = new String(bb, "GB2312");
			textGB2312 = URLEncoder.encode(textGB2312, "GB2312");
		} catch (UnsupportedEncodingException e1) {
			e1.printStackTrace();
			Log.e(TAG, "change text from UTF8 To GB2312 Error==" + e1.getMessage());
		}
		return textGB2312;
	}

